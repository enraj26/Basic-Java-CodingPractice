package com.codewithraju.basicpractice;


public class Swapping {
	
	public static void main(String[] args) {
		int a = 100;	// a = 100
		int b = 200;	// b = 200
		int c = a;		// c = 100
		a = b;			// a = 200
		b = c;			// b = 100
		System.out.println(a); //a =200
		System.out.println(b); // b  = 100
	}
}
