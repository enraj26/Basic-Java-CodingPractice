package com.codewithraju.basicpractice;


public class Swap2 {
	
	public static void main(String[] args) {

		int a = 50;		// a= 50
		int b = 250;	// b =250
		int c = 500;	// c =500
		int d = a;		// d = 50
		a = b;			// a = 250
		b = c;			// b = 500
		c = d;			// c = 50
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
	}

}
