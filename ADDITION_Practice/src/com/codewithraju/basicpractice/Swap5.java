package com.codewithraju.basicpractice;


public class Swap5 {

	public static void main(String[] args) {

		int a = 30;
		int b = 40;
		int c = 50;
		int d = a;
		a = b; // a = 40
		b = c; // b = 50
		c = d; // c = 30
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
	}

}
